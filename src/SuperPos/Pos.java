    private void txtprcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtprcodeKeyPressed
        // Check if the Enter key is pressed
    if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        String name = txtprcode.getText().trim();

        // If the barcode field is empty, move to the pay field
        if (name.isEmpty()) {
            txtpay.requestFocus();
        } else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/pos?useSSL=false", "root", "0704");
                insert = con1.prepareStatement("select * from product where barcode = ?");
                insert.setString(1, name);
                rs = insert.executeQuery();

                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Barcode not Found");
                } else {
                    String productname = rs.getString("product");
                    String price = rs.getString("retal_price");

                    txtprname.setText(productname.trim());
                    txtprice.setText(price.trim());
                    txtqty.requestFocus(); // Move the cursor to the quantity field
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    }//GEN-LAST:event_txtprcodeKeyPressed

