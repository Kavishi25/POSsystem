    private void Pos() {
    String name = txtprcode.getText();

    try {
        Class.forName("com.mysql.jdbc.Driver");
        con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/pos?useSSL=false", "root", "0704");
        insert = con1.prepareStatement("select * from product where barcode = ?");
        insert.setString(1, name);
        rs = insert.executeQuery();

        while (rs.next()) {
            int currentqty = rs.getInt("qty");

            int price = Integer.parseInt(txtprice.getText());
            int qtynew = Integer.parseInt(txtqty.getText());

            int tot = price * qtynew;

            if (qtynew > currentqty) {
                JOptionPane.showMessageDialog(this, "Available Product = " + currentqty);
                JOptionPane.showMessageDialog(this, "Quantity is not Enough");
            } else {
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(new Object[] {
                    txtprcode.getText(),
                    txtprname.getText(),
                    txtprice.getText(),
                    txtqty.getText(),
                    tot,
                });

                int sum = 0;
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
                }

                txtsub.setText(Integer.toString(sum));
            }
        }

        // Clear the input fields after processing
        txtprcode.setText("");
        txtprname.setText("");
        txtprice.setText("");
        txtqty.setText("");
        txtprcode.requestFocus();

    } catch (ClassNotFoundException ex) {
        Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Please enter valid numbers for price and quantity.");
    }
}

